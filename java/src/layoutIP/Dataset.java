	public static double[][][] getRandomPolygons(Random ran, double[][][] protos, int num, double s1, double s2, boolean shake) {
		double[][][] polys = new double[num][][];

		for (int i = 0; i < num; i++) {
			int id = ran.nextInt(protos.length);
			double[][] arr = new double[protos[id].length][];
			double sc = s1 + ran.nextDouble() * s2;

			for (int j = 0; j < arr.length; j++) {
				arr[j] = protos[id][j].clone();
				M._scale(sc, arr[j]);
			}

			polys[i] = arr;
		}

		if (shake) {
			for (int i = 0; i < num; i++) {
				for (int j = 0; j < polys[i].length; j++) {
					polys[i][j][0] += (ran.nextDouble() - 0.5) * 0.25 * s1;
					polys[i][j][1] += (ran.nextDouble() - 0.5) * 0.25 * s1;
				}
			}
		}

		return polys;
	}
